#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	chiffre;

	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	chiffre = (n % 10) + '0';
	ft_putchar_fd(chiffre, fd);
}
