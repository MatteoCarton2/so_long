#include "libft.h"

int	ft_str(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i] != '\0')
	{
		ft_char(str[i]);
		i++;
	}
	return (i);
}
