#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	size_t	lengthtotal;
	int		i;
	int		j;
	char	*str;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	lengthtotal = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = malloc(lengthtotal * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}
